# Operadores de comparación
# Mayor >
print(7 > 3)  # True
print(3 > 7)  # False
print(7 > 7)  # False

# Menor <
print(5 < 6)  # True
print(6 < 5)  # False
print(5 < 5)  # False

# Mayor o igual
print(2 >= 1)  # True
print(2 >= 3)  # False
print(2 >= 2)  # True

# Menor o igual
print(2 <= 1)  # False
print(2 <= 3)  # True
print(2 <= 2)  # True

# Igualación ==
print(6 == 6)  # True
print(5 == 2)  # False

# Diferente !=
print(6 != 6)  # False
print(5 != 2)  # True


# Diferente !=
print("Apple" == "Apple")  # True
print("Apple" == "apple")  # False
print("1" == 1)  # False

age = 18
print(18 >= age)
